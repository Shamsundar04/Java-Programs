package in.interview;

public class EscapeExample {
    public static void main(String[] args) {
        System.out.println("Hello\nWorld");      // New line
        System.out.println("Java\tProgramming"); // Tab space
        System.out.println("I\'m learning Java"); // Single quote
        System.out.println("He said, \"Hi\"");   // Double quote
        System.out.println("Backslash: \\");     // Backslash
        System.out.println("Unicode a: \u0041"); // Unicode for 'A'
    }
}
