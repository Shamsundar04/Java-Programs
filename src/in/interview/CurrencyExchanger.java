package in.interview;

public class CurrencyExchanger {

	public static void main(String[] args) {
		
		double dollertoInr=83.30;
		double amount=10;
		System.out.println(dollertoInr*amount);
		
//		833.0

		double inrtoDoller=1/dollertoInr;   // 1/83 
		amount=833;
		System.out.println(inrtoDoller*amount);
		
		
	}
}
